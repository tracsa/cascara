<template>
  <div
    class="full-columns"
    :class="containerClass">
    <div class="row">
      <div
        v-if="loading"
        :class="{ 'd-none d-md-block': selectedId }"
        class="col">
        <div class="card">
          <div class="card-header">
            <div style="float:right;">
              <a href="javascript:void(0);" @click="loadList">
                <icon :icon="['fas', 'sync-alt']" />
              </a>
            </div>
            {{ $t('trackings.trackings') }}
          </div>
          <div class="container-error">
            <div
              v-for="error in errors"
              class="alert custom-alert-danger">
              {{ $t(`errors.${error.where}`) }}
            </div>
          </div>
          <message-info
            :show="!trackings.length"
            icon="inbox"
            title="info.aboutTrackings"
            desc="info.aboutTrackingsMore"
          />

          <ul
            v-if="trackings.length"
            class="activity-list">
            <li
              :class="{ active: selectedId === tracking.execution.id }"
              v-for="tracking in trackings"
              :key="tracking.execution.id">
              <router-link
                :to="{
                  name: 'tracking',
                  params: { id: tracking.execution.id },
                }">
                <div class="activity-name">
                  {{ tracking.execution.name }}
                </div>
                <div class="activity-caret">
                  <icon :icon="['fas', 'caret-right']" />
                </div>
              </router-link>
            </li>
          </ul>
        </div>
      </div>
      <div
        v-else
        class="col d-none d-md-block">
        <loading />
      </div>

      <div v-if="selectedId" class="col-12 col-md-8">
        <tracking :id="selectedId" />
      </div>

    </div>
  </div>
</template>

<script>
import { get } from '@/utils/api';

export default {
  props: ['model'],
  data() {
    return {
      loading: true,
      trackings: [],
    };
  },
  mounted() {
    this.loadList();
  },
  methods: {
    loadList: function loadList() {
      this.loading = true;
      get('/activity')
        .then((body) => {
          this.loading = false;
          this.trackings = body.data;
        })
        .catch((errors) => {
          this.loading = false;
          // Alert about this
          console.error(errors);
        });
    },
  },
  computed: {
    selectedId: function selectedId() {
      const { id } = this.$route.params;
      if (!id) {
        return null;
      }

      return id;
    },
    containerClass: function containerClass() {
      return {
        container: this.selectedId === null,
        'container-fluid': this.selectedId !== null,
      };
    },
  },
};
</script>

